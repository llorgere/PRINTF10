#include "libftprintf.h"

char	*ft_p_conv_null(void)
{
	char*tab;

	if (!(tab = (char *)malloc(sizeof(*tab) * (4))))
		return (0);
	tab[0] = '0';
	tab[1] = 'x';
	tab[2] = '0';
	tab[3] = '\0';
	return (tab);
}
